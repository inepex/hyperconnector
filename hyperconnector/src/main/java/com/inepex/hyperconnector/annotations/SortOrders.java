package com.inepex.hyperconnector.annotations;

/**
 * Defines the possible sort order types of values.
 * @author Gabor Dicso
 *
 */
public enum SortOrders {
	/**
	 * Indicates that the possible values are sorted ascending.
	 */
	ASCENDING,
	/**
	 * Indicates that the possible values are sorted descending.
	 */
	DESCENDING
}
